package ifElseStatements;

public class ifElseClass2RelationalOperators {

	public static void main(String[] args) {
				
		int a = 10,
			b = 20;
	/* Relational Operators
	 * < , > , <= , >= , == , != 
	 */
		System.out.println(a!=b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a<b);
		System.out.println(a>b);
	}

}
